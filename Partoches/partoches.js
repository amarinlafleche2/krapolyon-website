function affiche(t,l)
{
  for(i=0;i<l;i++)
  {
    affiche_morceau(t[arity*i],t[arity*i+1],t[arity*i+2],t[arity*i+3],t[arity*i+4],t[arity*i+5],t[arity*i+6]);
  }
}


